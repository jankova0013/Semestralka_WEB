    <h2 id="home">  Vítejte na stránkách 3. ročníku Environmentální konference </h2>
        
    Třetí ročník konference je zaměřen na sucho a klimatickou změnu, neboť v současnosti není naléhavější výzvy, na kterou by se měly environmentální výchova a poradenství zaměřit. Pojďme společně promyslet, kam má náš obor směřovat, abychom nezůstali NASUCHO.

        V rámci konference zazní příspěvky v následujících tematických blocích:

        Proč je sucho a jaký vývoj lze očekávat – informace od odborníků přednesené srozumitelným jazykem.
        Jak informovat/vzdělávat o změně klimatu a souvisejících problémech žáky škol, aby byli motivováni jít studovat související profese. Jak informovat starosty obcí, aby věděli, kam se obrátit o radu s řešením komplikací a s možnými (přírodě blízkými) opatřeními v jejich regionu.
        Příklady konkrétních řešení – v obcích, městech, v zemědělství, v lesích, v krajině, na tocích.
        Konference je určena jak pracovníkům středisek a organizací zabývajících se environmentální výchovou nebo poradenstvím, tak i pracovníkům státní správy a samosprávy, pedagogům všech stupňů a typů škol, a další odborné veřejnosti.

        Akci připravují Lipka – školské zařízení pro environmentální vzdělávání a Ekologický institut Veronica, zadavatelem je Ministerstvo životního prostředí. Roli průvodce konferencí přijal prof. RNDr. Bedřich Moldan, CSc., dr. h. c.)

        Konference opět volně rozšíří program Mezinárodního filmového festivalu Ekofilm, který se uskuteční v Brně ve dnech 9. – 12. října 2019. Účast na konferenci lze tedy snadno spojit s návštěvou festivalových projekcí a přednášek. (viz WWW.EKOFILM.CZ)
